      SUBROUTINE CERO00T(ELEL2,ELEL1,NINDI)
C***********************************************************************
C
C****THIS ROUTINE INITIALIZE ELEL2 AND ELEL1
C    
C***********************************************************************
      IMPLICIT REAL*8(A-H,O-Z)
C
      DIMENSION  ELEL2(*),  ELEL1(*)
