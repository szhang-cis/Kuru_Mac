      SUBROUTINE  PLEQF6(CAPAP,PREY,PEND,SIKMA)
C************************************************************
C***  CURVA PARA ELEMENTOS DE FRICCION
C***********************************************************
      IMPLICIT REAL*8 (A-H,O-Z)
C
      PREY=(1.0/(1.0-CAPAP))-1.0+SIKMA
      PEND=1.0/(1.0-CAPAP)**2.0
      RETURN
      END
